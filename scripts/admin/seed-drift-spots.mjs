#!/usr/bin/env node
/**
 * 從 drift/app.js 抽出 SPOTS 陣列，轉成 SQL INSERT，輸出到 stdout。
 *
 * 用法：
 *   node scripts/admin/seed-drift-spots.mjs > worker/seed/drift-spots.sql
 *
 * 然後套用到遠端 D1：
 *   cd worker && npx wrangler d1 execute dropinn-db --remote --file seed/drift-spots.sql
 *
 * 這支腳本只負責「一次性匯入」既有寫死資料。之後新增/編輯 spots
 * 走 worker 的 /api/drift/spots CRUD，不會再用到這支。
 */

import fs from 'node:fs';
import vm from 'node:vm';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const appJsPath = path.resolve(__dirname, '../../drift/app.js');
const src = fs.readFileSync(appJsPath, 'utf8');

// 抓 `const SPOTS = [ ... ];` 整段 array literal
const match = src.match(/const SPOTS = (\[[\s\S]*?\n\]);/);
if (!match) {
  console.error('ERROR: SPOTS array not found in drift/app.js');
  process.exit(1);
}

// 用 vm 隔離 eval array literal（不會碰到 app.js 其他內容）
const spots = vm.runInNewContext(match[1]);

const now = new Date().toISOString();
const sqlEsc = (v) => String(v ?? '').replace(/'/g, "''");

const lines = [];
lines.push(`-- ${spots.length} spots seeded from drift/app.js at ${now}`);
lines.push(`-- Generated by scripts/admin/seed-drift-spots.mjs — do not edit by hand`);
lines.push('');

for (const s of spots) {
  const tagsJson = JSON.stringify(s.tags || []);
  const vals = [
    `'${sqlEsc(s.id)}'`,
    `'${sqlEsc(s.type)}'`,
    `'${sqlEsc(s.cat || '')}'`,
    `'${sqlEsc(s.name)}'`,
    `'${sqlEsc(s.area || '')}'`,
    Number(s.rating ?? 0),
    `'${sqlEsc(s.price || '')}'`,
    `'${sqlEsc(s.note || '')}'`,
    `'${sqlEsc(s.feature || '')}'`,
    `'${sqlEsc(tagsJson)}'`,
    s.nearby ? 1 : 0,
    Number(s.lat ?? 0),
    Number(s.lng ?? 0),
    `'${sqlEsc(s.status || 'open')}'`,
    s.noLoc ? 1 : 0,
    'NULL',           // displayOrder
    `'owner'`,        // createdBy
    `'${now}'`,
    `'${now}'`,
  ].join(', ');

  lines.push(
    `INSERT OR REPLACE INTO drift_spots ` +
    `(id, type, cat, name, area, rating, price, note, feature, tags, nearby, lat, lng, status, noLoc, displayOrder, createdBy, createdAt, updatedAt) ` +
    `VALUES (${vals});`
  );
}

console.log(lines.join('\n'));
console.error(`✓ Generated ${spots.length} INSERT statements`);
